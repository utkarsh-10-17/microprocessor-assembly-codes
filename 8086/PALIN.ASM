DATA SEGMENT

BLOCK1 DB 'DELEVELED' ; 
MSG1 DB "IT IS PALINDROME $"
MSG2 DB "IT IS NOT PALINDROME $"
PAL DB 00H

DATA ENDS

PRINT MACRO MSG

MOV AH,09H
LEA DX,MSG
INT 21H
INT 3H

ENDM

EXTRA SEGMENT

BLOCK2 DB 9 DUP(?) ; Adjust the length of BLOCK2 to match the new input
EXTRA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA,ES:EXTRA
START: MOV AX,DATA
MOV DS,AX
MOV AX,EXTRA
MOV ES,AX
LEA SI,BLOCK1
LEA DI,BLOCK2+8 ; Adjust the destination index to the end of BLOCK2
MOV CX,0009h ; Adjust the loop count to match the length of the new input
BACK: CLD
LODSB
STD
STOSB
LOOP BACK
LEA SI,BLOCK1
LEA DI,BLOCK2
MOV CX,0009h ; Adjust the loop count here as well
CLD
REPZ CMPSB
JNZ SKIP
PRINT MSG1
JMP END_PRINT
SKIP: PRINT MSG2
END_PRINT:
CODE ENDS
END START
